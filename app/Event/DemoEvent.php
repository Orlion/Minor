<?php

namespace App\Event;

use Minor\Event\Event;

class DemoEvent extends Event
{
    private $name;

    public function __construct($name)
    {
        $this->name = $name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }
}